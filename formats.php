                <h2>FORMAT, dessins et sculptures</h2>
                <p>Exposition personnelle
                <br />Du 9 juin au 15 juillet 2012
                <br />Centre Albert Chanot, centre d'art à Clamart
                <br />33, rue Brissart
                <br />92 140 Clamart
                <br />Responsable Artistique : Madeleine Mathé
                <br />avec l'aide deChloé Esdraffo, Gaspard Resve et Jérémy Nonotte, Romaric Fillette
                </p>
                <p>L'exposition est construite en 3 temps. Le premier est le seuil, un narthex généreusement lumineux. On commence par un résumé de l'exposition.<br />
                - un tirage photographique de l'image qui fait l'affiche et le carton, « porte dressée »<br />
                - une maquette de l'installation dans la grande salle<br />
                - un chariot de caddies<br />
                - un chariot de planches<br />
                La maquette, le tirage, les documents contenus dans les caddies et les planches respectent un format unique, 18x24 cm. C'est un format photographique, il contraint chacun de mes documents depuis 1996, il est le premier critère objectif d'un ensemble de règles qui régissent le document comme syntaxe et grammaire pour l'écrit. C'est un outil de prospection (recherche, démarchage, rencontre).</p>
                <p>En 2009 quand j'accède à un vaste atelier je peux tout d'abord rassembler la quasi-
                totalité de mes archives, principalement l'ensemble de mes caddies, une petite partie ici.<br />
                Ils contiennent des travaux, expériences, des histoires aussi parfois. Peuvent être autonomes comme celui sur les lampes (1997), un autre contient les peintures réalisées sur le chantier de la Halle aux farines (2005/2007), d'autres contiennent des aquarelles, dessins, maquettes, gravures … fragments et expériences en jachère.<br />
                Chacune de ces caisses tient sur ce châssis à roulettes pour constituer un caddie.</p>
                <p></p>


                <h3>Sculptures</h3>
                <p>Encouragé par Stéphane Doré je souhaite réaliser des développements à partir d'un travail de dessins au format 18x24, les aquarelles au bleu de Lectoure, c'est très naturellement que le format devient la matière assemblée. Le second chariot porte des planches en mélaminé blanc d'épaisseur 16 et 18 mm. Elles sont issues de ce que Léda m'a aidé à comprendre comme le fruit d'un logarithme de fabrication, une suite finie d'opérations finies :<br />
                - récolte des planches sur les trottoirs dans un rayon de 3 à 7 km<br />
                - mise à nu des planches pour les débarrasser de leur quincaillerie<br />
                - nettoyage<br />
                - débit au format<br />
                - classement par matière (agglo, CP, latté, massif …) aspect de surface, épaisseur<br />
                Wikipédia nous dit qu'un logarithme transforme un produit en somme et une puissance en produit. Une somme de produits, on n'est pas loin de la matière que visent à inventer ces opérations.</p> 
                <p>1 - Une première expérience en volume est réalisée en 2010, « Angle à facettes » lors de l'exposition « HZexpo » que je monte à l'atelier. C'est une première sculpture d'une série, première étape d'un apprentissage entre architecture et sculpture. Entre architecture et sculpture il y a la gravité dont il s'agit sinon de s'affranchir du moins de l'apprivoiser. Ainsi l'angle à facettes a la ceinture et les bretelles, repose solidemennt au sol et s'érige en s'appuyant sur les murs.</p>
                <p>2 - Ensuite viennent la voûte et les roues qui explorent les mécaniques qui transforment la feuille en un espace. Ces 2 sculptures s'appuient sur nécessité d'usage (abriter sous une voûte ou rouler) pour explorer l'espace public. Les livrets en rendent compte.</p>
                <p>3 - Le serpent qui se love dans le coin de la galerie déplace, libère et clarifie la recherche. La forme peut n'être qu'un contour et quand le nez du serpent se soulève de la piste de l'aérodrome on sympathise un peu avec la gravité.</p>
                <p>4 - La matière est éprouvée sur les machines de l'atelier et les molécules sont autant d'assemblages mécaniques mesurés selon la qualité de l'assemblage et l'espace qu'il génère. L'une de ces pièces est à l'origine de la baleine montée et exposée à l'atelier en 2011. L'assemblage n'a ni verticale ni horizontale mais peut se développer et contaminer un volume important. Quand un soir Gaspard m'aide à la gonfler notre objectif est de la pousser jusque son état limite, j'ai l'impression d'être un sculpteur qui ajoute avec le pouce un peu de glaise, en un peu plus grand. Le lendemain quand j'arrive à l'atelier Gaspard est en train de changer quelques pièces faibles, en limite de rupture. On entend la grosse bête craquer une fois, deux fois puis s'effondrer doucement. Qu'à cela ne tienne, il glisse une traverse en acier dedans et nous la suspendons au palan grâce auquel j'avais pu la monter, et elle décolle et s'envole. Quand j'envoie cette photo à Jean il me dit « au commencement était la chute».</p>

                <p>La première salle est dédiée aux grands dessins. Une pratique du dessin est au centre de mon travail, des dessins dans des carnets et sur des feuilles comme celles accrochées ici. Un CD joint aux livrets rassemble les plus de 10 000 fichiers organisés en une base de données. Les encres présentes ont en commun avec les sculptures — au delà du format — un fonctionnement mécanique interne, une technique de lavis en glacis, la mécanique externe est la reproduction en multiple d'un motif.</p>
                <p>La porte dressée échappe au 18x24, à la mesure elle ajoute la question de l'échelle, rapport entre la mesure d'un objet réel et la mesure de sa représentation. Nous y reviendrons plus tard.</p>
                <h3>Paysage profond</h3>
                <p>Lorsqu'il s'est agi d'investir cette salle j'ai souhaité travailler à ce qui me semble en faire la principale qualité, sa profondeur.</p>
                <p>Trois pièces tendent l'espace. La première est la perspective profonde. Les fils reprennent la trame du carrelage et la fait converger vers un point à ma hauteur d'œil haut, 1,70 m.</p>
                <p>Les deux autres pièces réalisées en 18x24 représentent sensiblement la même quantité de planches, environ 7,20 m.<br />
                Le paysage a été réalisé en octobre 2011 pour l'école d'architecture Paris-Malaquais où il avait été monté selon une autre configuration. Il assemble des planches de contreplaqué à du stratifié compact. Le stratifié n'est pas altéré, le contreplaqué percé/fendu à deux angles qui permettent l'assemblage délégué.<br />
                - les planches vertes doivent toujours être horizontales mais l'assemblage est libre, le mode d'assemblage est mécanique mais ludique comme un jeu de construction, c'est pourquoi j'ai demandé à Gaspard et Rémus de jouer avec.<br />
                - la configuration est topographique et paysagère avec ses vallons, ses lignes, son relief et ses circulations.<br />
                - elle s'arrête quand elle est à le mesure de l'espace dans laquelle elle est montée.</p>
                <h3>Le trône</h3>
                <p>Quand j'ai démonté la baleine je me suis trouvé avec une grande quantité de planches. Je les ai rangées sur un grand chariot et j'ai voulu m'asseoir dedans. La structure est un châssis, une planche de stratifié compact, des tiges filetées solidaires sur lesquelles comme des perles viennent s'enfiler les planches percées en leur centre.</p>
                <p>La mécanique de cette masse mobile décrit non pas un protocole mais un mode d'emploi. Les planches ont une épaisseur de16, 17 et 18 mm, elles sont assemblées en lits de même épaisseur pour dessiner une altimétrie réglée. Le système qui permet de les distinguer rapidement (épaisse, moyenne ou fine) est un procédé de couleur et de matière.<br />
                Peindre les planches est une stratégie d'encapsulation : protéger et isoler des émanations d'aldéhydes, cacher une information et regrouper dans un système modulaire.</p>
                <h3>Un enjeu de peinture</h3>
                <p>J'ai peur de faire un trône mais Gaston me dit que «parfois faut pas lutter, si c'est un trône c'est un trône». Alors je décide de le dorer comme un trône. Matériellement les épaisseurs des planches correspondent à 1, 2 ou 3 rainures verticales sur le chant. Les creux sont bombés de rouge ce qui évoque la dorure à l'assiette qui relève la luminosité de l'or qui recouvre. Les ors vont avec les pourpres.<br />
                Dans ce fauteuil, on s'assoit. On y est plutôt bien. Quand je dois réfléchir au rendez-vous d'aujourd'hui je regrette de ne pas l'avoir. On y est à la bonne hauteur, pas trop enfoncé, le dos est droit sur le dossier un peu souple. Madeleine s'y assoit, elle dit qu' «on y a l'impression d'être quelqu'un d'important».</p>
                <p>Reste une pièce, le caddie. C'est ce caddie que je colporte depuis de nombreuses années. Dedans la place de ranger des formats 18x24. Françoise Baudin m'a raconté ce qu'elle fait à Fontevraud, entre autres une soirée « Nuit » lors de laquelle les habitants alentours étaient invités à profiter d'une exposition puis choisir l'endroit où dormir pour assister au réveil au spectacle de Bartabas qui sur son cheval accompagne le lever du soleil. La performance est un accomplissement, un achèvement même s'il est à recommencer.</p>
                <p>– J'ai voulu y ranger des outils pour faire un trou dans le mur du fond parce que si ce format mène à tout il n'y a pas de raison que ce ne soit pas à condition d'en sortir. Le 18x24 est alors une fenêtre dans le mur, il existe à travers son absence et à ce moment là nous en sortons au sens littéral.<br />
                – Quand j'ai dû nommer le rendez-vous je l'ai appelé «trône», imaginant que pourraient être photographiés tous ceux qui veulent s'asseoir dans le trône à ma place.<br />
                – La porte dressée comme je disais tout à l'heure ajoute à la mesure l'échelle. Ceux qui ont vu l'image du carton ont parfois pensé à un ballon, certains auront cru que c'était une bille, d'autres une perle. Sortir du format c'est aussi sortir de ce paysage qui agglomère cette histoire, aller dans le jardin, dresser cette échelle qui nous permet de nous frotter à la gravité et de cueillir des cerises sans tomber.
                Après la performance il y a un goûter de cerises, youpi.</p>
                <p>Texte de la performance du 01/07/2012 au centre Albert Chanot</p>
                
